public class Polyspirals{

  public static void main(String[] args){
    Turtle t = new Turtle(new TurtleWorld(1200, 900));
    polyspiral(t, 4, 3);
    t.penup();
    t.forward(150);
    t.pendown();
    polyspiral(t, 4, 4);
    t.penup();
    t.forward(300);
    t.pendown();
    polyspiral(t, 4, 5);
  }

  public static void polyspiral(Turtle t, int rounds, int n){
    for (int i = 0; i < rounds*n; i++) {  //number of segments of spiral
      t.forward((i + 1) * 10);
      t.right(360.0/n);
    }
    for (int i = rounds*n - 1; i >= 0; i--) { //returning to the original position
      t.left(360.0/n);
      t.backward((i + 1) * 10);
    }
  }
}

